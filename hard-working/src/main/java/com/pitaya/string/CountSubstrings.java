public class CountSubstrings {
    public int countSubstrings(String s) {
        // ?????
        int ans = 0;
        for (int center = 0; center < 2 * s.length() - 1; center++) {
            // left?right???????????
            // ???left????????2??????????right????left????????????????????????
            // ?????????????????????????????
            int left = center / 2;
            int right = left + center % 2;

            while (left >= 0 && right < s.length() && s.charAt(left) == s.charAt(right)) {
                ans++;
                left--;
                right++;
            }
        }
        return ans;
    }
}


