package com.pitaya.linkedlist.totate_right;

/**
 * @Description:
 * @Date 2023/09/19 07:16:00
 **/
public class ListNode {
    int val;
    ListNode next;
    ListNode() {}
    ListNode(int val) {
        this.val = val;
    }
    ListNode(int val, ListNode next){
        this.val = val;
        this.next = next;
    }
}
